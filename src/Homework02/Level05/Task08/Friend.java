package Homework02.Level05.Task08;

public class Friend {
    private String name;
    private int age;
    private String sex;

    public Friend(String name) {
        this.name = name;
    }

    public Friend(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public Friend(String name, int age, String sex) {
        this.name = name;
        this.age = age;
        this.sex = sex;
    }
}
